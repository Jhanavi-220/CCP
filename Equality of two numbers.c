                                                     //Equality of two numbers
#include <stdio.h>
void main()
{
 int a,b;
 printf("Enter two numbers=");
 scanf("%d%d",&a,&b);
 if (a==b)
 {
  printf("Both the numbers are equal");
 }
 else
 {
  printf("Both the numbers are not equal");
 }
 return 0;
}
